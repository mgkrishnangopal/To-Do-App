const list_of_items=[];
const addNewTask= (tasktitle,completestatus) => {
if(tasktitle===""){
    console.warn("Inputs Cannot Be Empty");
    return;
};


list_of_items.push({
tasktitle,
completestatus
});
console.log(list_of_items);
};

const displayTasks = () => {
if (list_of_items.length === 0) {
    tasklistt.textContent = " You Don't Have Any Pending Tasks.......!";
    return;
}
tasklistt.innerHTML="";

list_of_items.forEach(e => {
     
    const title= document.createElement("section");
    title.classList.add("task-title");
    title.innerHTML=e.tasktitle;

    const delBtn = document.createElement("button");
    delBtn.classList.add("del-btn");
    delBtn.innerHTML =deltaskIcon;

    const EditBtn= document.createElement("section");
    EditBtn.classList.add("edit-btn");
    EditBtn.innerHTML=e.edittaskIcon;
    
    delBtn.addEventListener("click", () => {
            list_of_items.splice(list_of_items.indexOf(e), 1);
            displayTasks();
        });
        EditBtn.addEventListener("click", () => {
            tasklistt.textContent=list_of_items.indexOf(e);
            displayTasks();
        });



  
    const tasks=document.createElement("section");
    tasks.classList.add("tasks");

    tasks.appendChild(title);
    tasks.appendChild(delBtn);
    tasks.appendChild(EditBtn);
tasklistt.appendChild(tasks);
console.log(tasklistt);
});

};




addtask.addEventListener ("click", () => {
let newtask = inputtask.value ;
addNewTask(newtask,false);

 displayTasks();

inputtask.value= "";
inputtask.focus();



});